package Model;

/**
 *
 * @author Admin
 */
public class Ban {

    private int maBan;
    private String tenBan;
    private int maKhuVuc;
    private String ghiChu;

    public Ban() {
    }

    public Ban(int maBan, String tenBan, int maKhuVuc, String ghiChu) {
        this.maBan = maBan;
        this.tenBan = tenBan;
        this.maKhuVuc = maKhuVuc;
        this.ghiChu = ghiChu;
    }

    public int getMaBan() {
        return maBan;
    }

    public void setMaBan(int maBan) {
        this.maBan = maBan;
    }

    public String getTenBan() {
        return tenBan;
    }

    public void setTenBan(String tenBan) {
        this.tenBan = tenBan;
    }

    public int getMaKhuVuc() {
        return maKhuVuc;
    }

    public void setMaKhuVuc(int maKhuVuc) {
        this.maKhuVuc = maKhuVuc;
    }

    public String getGhiChu() {
        return ghiChu;
    }

    public void setGhiChu(String ghiChu) {
        this.ghiChu = ghiChu;
    }
}
